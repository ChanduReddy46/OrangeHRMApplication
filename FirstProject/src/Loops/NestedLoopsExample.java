package Loops;
public class NestedLoopsExample {

	public static void main(String[] args) {
//Nested loops
		for(int a=0;a<=2;a++) //outer Loop
		{
			for(int b=1;b<=2;b++) //inner Loop
			{
				System.out.println("Selenium");
			}
		}
		System.out.println("#######2#######");
		System.out.println();
		for(int a=0;a<=2;a++)
		{
			for(int b=1;b<=2;b++)
			{
				System.out.println(b);
			}
		}
		System.out.println("#######3######");
		System.out.println();
		for (int a=0;a<2;a++)
		{
			for(int b=1;b<=2;b++)
			{
			System.out.println(a+b);
			}
		}
		System.out.println("###########4##########");
		System.out.println();
		for(int a=0;a<=2;a++)
		{
			for(int b=1;b<=2;b++)
			{
				System.out.println("Testing");
				System.out.println();
			}
		}
			System.out.println("#######5#########");	
			System.out.println();
			for(int a=0;a<=6;a++)
			{
				for(int b=1;b<=6;b++)
				{
					System.out.println("10");
				System.out.println();
				}
			}
	System.out.println("##############6##########");
	System.out.println();
	for(int a=0;a<2;a++)
	{
		for(int b=1;b<=2;b++)
		{
			System.out.println("9");
			System.out.println();
		}
	}
	System.out.println("#############");
	System.out.println();
	for(int a=1;a<=2;a++)
	{
		for(int b=1;b<=2;b++){
			{
				System.out.println("5");
				System.out.println();
			}
		}
		
	}
	}
	
	
	
	

}
